package whileLoop;

/* For you to do:
    Using while loop print numbers from 0 to 8

    **Output:**
    0
    1
    2
    3
    4
    5
    6
    7
    8
 */
public class FortySix {
    public static void main(String[] args){
        int counter=0;
        while (counter<=8){
            System.out.println(counter);
            counter++;
        }
    }
}
