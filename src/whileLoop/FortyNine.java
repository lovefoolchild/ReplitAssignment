package whileLoop;

/* For you to do:**
    Write a java program to print  "Hello Syntax Team! How are you?"  three times using the while loop
 */
public class FortyNine {
    public static void main(String[] args) {
        int counter = 1;
        while (counter<=3) {
            System.out.println("Hello Syntax Team! How are you?");
            counter++;
        }
    }
}
