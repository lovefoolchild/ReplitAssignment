package creatingVariablesAndPrinting;

public class ZeroZeroFour {
    public static void main(String[] args) {
        /*
        **For you to do:**
        Print out the following pattern:
        *
        **
        ***
        ##
        #

        **Output:**
        *
        **
        ***
        ##
        #
         */
        char asterisk='*';
        String doubleAsterisk="**";
        String tripleAsterisk="***";
        String doubleNumber="##";
        char number='#';
        System.out.println(asterisk);
        System.out.println(doubleAsterisk);
        System.out.println(tripleAsterisk);
        System.out.println(doubleNumber);
        System.out.println(number);
    }
}
